    with engine.connect() as conn:
        # 1. Add name_zh column if not exists (SQLite compat)
        from sqlalchemy import inspect
        insp = inspect(engine)
        cols = [c['name'] for c in insp.get_columns('jupas_programmes')]
        if 'name_zh' not in cols:
            conn.execute(text("ALTER TABLE jupas_programmes ADD COLUMN name_zh VARCHAR(500)"))
            conn.commit()

        # 2. Update each programme by jupas_code
        updated = 0
        skipped = 0
        for js_code, zh_name in JUPAS_ZH.items():
            result = conn.execute(
                text("""
                    UPDATE jupas_programmes
                    SET name_zh = :zh_name, updated_at = CURRENT_TIMESTAMP
                    WHERE jupas_code = :js_code
                """),
                {"zh_name": zh_name, "js_code": js_code},
            )
            if result.rowcount > 0:
                updated += 1
            else:
                skipped += 1

        conn.commit()
        print(f"Done. Updated: {updated}, Not found in DB (skipped): {skipped}")
        print(f"Total Chinese names available: {len(JUPAS_ZH)}")


